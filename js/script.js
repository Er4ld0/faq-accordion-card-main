function clickFaq1(){
    if (title1.style.display == 'block'){
        title1.style.display = 'none'
    } else{
        title1.style.display = 'block'
    }
    
    if (faq1.style.rotate == '-180deg'){
        faq1.style.rotate = '0deg'
    }   else{
        faq1.style.rotate = '-180deg'
    }

    if (faq1.style.transition == '0.5s'){
        faq1.style.transition = '0'
    }   else{
        faq1.style.transition = '0.5s'
    }
}

